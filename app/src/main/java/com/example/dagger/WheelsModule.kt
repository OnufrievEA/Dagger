package com.example.dagger

import dagger.Module
import dagger.Provides

@Module
class WheelsModule {

    @Provides
    fun provideReems(): Rims {
        return Rims()
    }

    @Provides
    fun provideTires(): Tires {
        val tires = Tires()
        tires.inflate()
        return tires
    }

    @Provides
    fun provideWheels(rims: Rims, tires: Tires): Wheels {
        return Wheels(rims, tires)
    }
}